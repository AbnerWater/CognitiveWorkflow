#!/usr/bin/env node
/**
 * generate-ts-schemas.mjs — JSON Schema → TypeScript 类型生成
 *
 * 输入：packages/schemas-ts/src/generated/json-schema/*.json（由 generate-json-schemas.py 产出）
 * 输出：packages/schemas-ts/src/generated/*.ts
 *
 * ADR-0003：手动改 packages/schemas-ts/src/generated/* 在 CI codegen-consistency 中会 fail。
 *
 * M1.1 stub：当前仅产出占位文件，确保 pipeline 能跑通；M1.2 引入实际 json-schema-to-typescript 调用。
 */

import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { existsSync, mkdirSync, readdirSync, writeFileSync } from "node:fs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..", "..");
const INPUT_DIR = join(ROOT, "packages", "schemas-ts", "src", "generated", "json-schema");
const OUTPUT_DIR = join(ROOT, "packages", "schemas-ts", "src", "generated");

mkdirSync(OUTPUT_DIR, { recursive: true });

if (!existsSync(INPUT_DIR)) {
  console.log(`[codegen] 缺 ${INPUT_DIR}；请先运行 \`uv run python scripts/codegen/generate-json-schemas.py\``);
  console.log("[codegen] M1.1 stub：跳过 TS 生成，不报错");
  process.exit(0);
}

const inputs = readdirSync(INPUT_DIR).filter((f) => f.endsWith(".json"));

if (inputs.length === 0) {
  console.log("[codegen] 无输入 JSON Schema；跳过 TS 生成");
  process.exit(0);
}

const banner = `// AUTO-GENERATED by scripts/codegen/generate-ts-schemas.mjs
// 不要手改本文件。修改方式：改 packages/schemas（Python cw_schemas）后运行 \`make codegen\`。

`;

for (const f of inputs) {
  const baseName = f.replace(/\.json$/, "");
  const tsPath = join(OUTPUT_DIR, `${baseName}.ts`);
  const tsBody = `${banner}export const ${camelize(baseName)}_SCHEMA_NAME = ${JSON.stringify(baseName)} as const;
`;
  writeFileSync(tsPath, tsBody, "utf8");
  console.log(`[codegen] ${baseName}.json → ${tsPath}`);
}

function camelize(s) {
  return s.replace(/[^a-zA-Z0-9]+(\w)/g, (_, c) => c.toUpperCase()).replace(/^([A-Z])/, (m) => m.toLowerCase());
}
