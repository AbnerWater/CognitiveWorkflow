#!/usr/bin/env node
/**
 * generate-ts-schemas.mjs — JSON Schema → TypeScript 类型生成
 *
 * 输入：packages/schemas-ts/src/generated/json-schema/*.json（由 generate-json-schemas.py 产出）
 * 输出：packages/schemas-ts/src/generated/*.ts
 *
 * ADR-0003：手动改 packages/schemas-ts/src/generated/* 在 CI codegen-consistency 中会 fail。
 */

import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import {
  existsSync,
  mkdirSync,
  readdirSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { compileFromFile } from "json-schema-to-typescript";
import * as prettier from "prettier";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..", "..");
const INPUT_DIR = join(
  ROOT,
  "packages",
  "schemas-ts",
  "src",
  "generated",
  "json-schema",
);
const OUTPUT_DIR = join(ROOT, "packages", "schemas-ts", "src", "generated");

mkdirSync(OUTPUT_DIR, { recursive: true });

if (!existsSync(INPUT_DIR)) {
  console.log(
    `[codegen] 缺 ${INPUT_DIR}；请先运行 \`uv run python scripts/codegen/generate-json-schemas.py\``,
  );
  process.exit(1);
}

const inputs = readdirSync(INPUT_DIR)
  .filter((f) => f.endsWith(".json"))
  .sort((a, b) => a.localeCompare(b, "en"));

if (inputs.length === 0) {
  console.log("[codegen] 无输入 JSON Schema");
  process.exit(1);
}

const banner = `/**
 * AUTO-GENERATED by scripts/codegen/generate-ts-schemas.mjs.
 * Do not edit by hand. Change packages/schemas (Python cw_schemas), then run make codegen.
 */
`;

for (const staleFile of readdirSync(OUTPUT_DIR).filter((f) =>
  f.endsWith(".ts"),
)) {
  rmSync(join(OUTPUT_DIR, staleFile));
}

const typeExports = [];

for (const f of inputs) {
  const baseName = f.replace(/\.json$/, "");
  assertTypeName(baseName);

  const schemaPath = join(INPUT_DIR, f);
  const tsPath = join(OUTPUT_DIR, `${baseName}.ts`);
  const tsBody = await compileFromFile(schemaPath, {
    bannerComment: banner,
    cwd: INPUT_DIR,
    enableConstEnums: false,
    style: {
      bracketSpacing: true,
      parser: "typescript",
      printWidth: 80,
      semi: true,
      singleQuote: false,
      tabWidth: 2,
      trailingComma: "all",
    },
    unknownAny: true,
  });
  const formattedTsBody = await prettier.format(tsBody, {
    filepath: tsPath,
    parser: "typescript",
  });

  writeFileSync(tsPath, formattedTsBody, "utf8");
  typeExports.push(`export type { ${baseName} } from "./${baseName}.js";`);
  console.log(`[codegen] ${baseName}.json → ${tsPath}`);
}

const barrelPath = join(OUTPUT_DIR, "index.ts");
const barrelBody = `${banner}
${typeExports.join("\n")}
`;
const formattedBarrelBody = await prettier.format(barrelBody, {
  filepath: barrelPath,
  parser: "typescript",
});
writeFileSync(barrelPath, formattedBarrelBody, "utf8");
console.log(`[codegen] generated barrel → ${barrelPath}`);

function assertTypeName(name) {
  if (!/^[A-Za-z_$][A-Za-z0-9_$]*$/.test(name)) {
    throw new Error(
      `JSON Schema filename must be a valid TypeScript identifier: ${name}`,
    );
  }
}
